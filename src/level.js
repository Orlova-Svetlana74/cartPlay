window.application = {
  levels: {},
  renderLevel: function (levelNumber) {
    window.application.levels[levelNumber];
    // очищаем контейнер перед отрисовкой экрана
    APP_CONTAINER.innerHTML = '';
    window.application.levels[levelNumber]();
  },
};

